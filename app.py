# Constants for label splits
BASECOLOUR_LEN = 46
ARTICLETYPE_LEN = 143
SEASON_LEN = 4
GENDER_LEN = 5

# Load model with caching
@st.cache_resource()
def load_trained_model():
    return load_model("final_model.keras")  # Update with actual model path if needed

model = load_trained_model()


st.title("üß† Fashion Image Classifier")
