uploaded_file = st.file_uploader("üìÅ Upload Image", type=["jpg", "jpeg", "png"])

def predict_image(img_path, model, label_columns):
    img = image.load_img(img_path, target_size=(128, 128))
    img_array = image.img_to_array(img) / 255.0
    img_array = np.expand_dims(img_array, axis=0)

    preds = model.predict(img_array)[0]

    # Slice predictions
    basecolour_preds = preds[:BASECOLOUR_LEN]
    articletype_preds = preds[BASECOLOUR_LEN:BASECOLOUR_LEN+ARTICLETYPE_LEN]
    season_preds = preds[BASECOLOUR_LEN+ARTICLETYPE_LEN:BASECOLOUR_LEN+ARTICLETYPE_LEN+SEASON_LEN]
    gender_preds = preds[BASECOLOUR_LEN+ARTICLETYPE_LEN+SEASON_LEN:]

    # Get predicted indices
    basecolour_idx = np.argmax(basecolour_preds)
    articletype_idx = np.argmax(articletype_preds) + BASECOLOUR_LEN
    season_idx = np.argmax(season_preds) + BASECOLOUR_LEN + ARTICLETYPE_LEN
    gender_idx = np.argmax(gender_preds) + BASECOLOUR_LEN + ARTICLETYPE_LEN + SEASON_LEN

    # Retrieve labels
    predicted_labels = [
        label_columns[basecolour_idx],
        label_columns[articletype_idx],
        label_columns[season_idx],
        label_columns[gender_idx],
    ]
    return predicted_labels

